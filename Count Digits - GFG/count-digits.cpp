class Solution{
public:
    int evenlyDivides(int N){
        //code here
        int num = N;
        int cnt=0;
        while(num>0){
            int n = num%10;
            if(n!=0 && N%n==0)cnt++;
            num = num/10;
        }
        return cnt;
    }
};

